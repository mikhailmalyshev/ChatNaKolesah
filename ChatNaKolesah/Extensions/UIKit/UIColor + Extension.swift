import Foundation
import UIKit

extension UIColor {
    
    static func buttonRed() -> UIColor {
        return .red
    }
    
    static func mainWhite() -> UIColor {
        return UIColor(red: 0.0, green: 189.0, blue: 171.0, alpha: 0.5)
    }
    
    static func buttonDark() -> UIColor {
        return UIColor(red: 0.0, green: 0.0, blue: 0.0, alpha: 0.8)
    }
    
    static func textFieldLight() -> UIColor {
        return .lightGray
    }
}
